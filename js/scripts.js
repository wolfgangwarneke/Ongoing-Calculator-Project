//MATH FUNCTIONS
//addition
var add = function(number1, number2) {
	return number1 + number2;
};

//subtraction
var sub = function(number1, number2) {
	return number1 - number2;
};

// multiplication
function times(number1, number2) {
	return number1 * number2;
}

//division
function div(number1, number2) {
	return number1 / number2;
}
//END MATH FUNCTIONS
